

var csurmulot = document.querySelector("#c1");
var surmulot = document.querySelector("#t1");
var vsurmulot = 1;

csurmulot.addEventListener("click", changementSurmulot);

function changementSurmulot(){
    if(vsurmulot == 1){
        csurmulot.style.filter = "grayscale(100%)";
        surmulot.style.display = "none";
        vsurmulot = 0;
    } else {
        csurmulot.style.filter = "grayscale(0%)";
        surmulot.style.display = "flex";
        vsurmulot = 1;
    }
}

